		string ticket;
		long long num, a1, a2, a3;
		long long a4[9][101];
		bool unique, sorted;
		int blanks, choices;
		string name;
		vector <Result> result;
		
		while (!rules.empty()) {
			ticket = rules.back();
			rules.pop_back();
			int t = ticket.find(':');
			name = ticket.substr(0, t);
			t = t + 2;
			choices = blanks = 0;
			while (ticket[t++] != ' ') choices = choices * 10 + ticket[t-1] - '0';
			while (ticket[t++] != ' ') blanks = blanks * 10 + ticket[t-1] - '0'; 
			sorted = (ticket[t] == 'T');
			unique = (ticket[t+2] == 'T');
			
			a1 = a2 = 1;
			for (int i = 1; i <= blanks; i++) a1 = a1 * choices;					//!unique[n] && !sorted[n]
			for (int i = choices; i > choices - blanks; i--) a2 = a2 * i;			//unique[n] && !sorted[n]
			a3 = a2;
			for (int i = 1; i <= blanks; i++) a3 /= i;	 							//unique[n] && sorted[n]
			
			if (!unique && !sorted) num = a1;
			else if (unique && !sorted) num = a2;
			else if (unique && sorted) num = a3;
			else {																	//!unique[n] && sorted[n]
				for (int i = 1; i <= choices; i++) a4[1][i] = 1;
				for (int i = 2; i <= blanks; i++)
					for (int j = 1; j <= choices; j++) {
						a4[i][j] = 0;
						for (int k = 1; k <= j; k++)
							a4[i][j] += a4[i-1][j-k+1] * 1; 
					}
				num = 0;
				for (int i = 1; i <= choices; i++) num += a4[blanks][i];
			}
			
			Result tmp;
			tmp.name = name;
			tmp.num = num;
			result.push_back(tmp);	
		}
		
		vector <string> re;
		sort(result.begin(), result.end());

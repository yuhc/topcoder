class CoinFlipsDiv2 {
public:
	int countCoins(string state) {
		int n = state.length();
		if (n <= 1) return 0;
		
		int ans = 0;
		if (state[0] != state[1]) ans = 1;
		for (int i = 1; i < n-1; i++)
			if (state[i] != state[i-1] || state[i] != state[i+1]) ans++;
	    if (state[n-1] != state[n-2])
	    	ans++;
	    	
	    return ans;		
	}
};


